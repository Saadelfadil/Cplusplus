#include "Cat.hpp"

/*
** ------------------------------- CONSTRUCTOR --------------------------------
*/

Cat::Cat()
{
	this->_type = "Cat";
	std::cout << "Hello from Cat CONSTRUCTOR ..." << std::endl;
}

Cat::Cat(const Cat & src )
{
	this->_type = src._type;
	*this = src;
}

/*
** -------------------------------- DESTRUCTOR --------------------------------
*/

Cat::~Cat()
{
	std::cout << "Hello from Cat DESTRUCTOR ..." << std::endl;
}

/*
** --------------------------------- OVERLOAD ---------------------------------
*/

Cat &	Cat::operator=(Cat const & rhs )
{
	this->_type = rhs._type;
	return *this;
}
